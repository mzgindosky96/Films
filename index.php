        let moviesData = [];
        let slugToMovie = new Map();
        let viewCounts = {};
        let currentLanguage = 'en';
        let currentFilter = 'all';
        let currentSearchQuery = '';
        let currentPage = 1;
        const ITEMS_PER_PAGE = 20;
        let currentView = 'home';
        let currentMovieSlug = null;

        // Helper: generate slug from title and id
        function generateSlug(title, id) {
            let slug = title
                .toString()
                .toLowerCase()
                .replace(/[^a-z0-9\s-]/g, '')
                .trim()
                .replace(/\s+/g, '-')
                .replace(/-+/g, '-');
            if (!slug) slug = 'movie';
            return `${slug}-${id}`;
        }

        // Load movies from PHP API
        async function loadMoviesFromAPI() {
            try {
                const response = await fetch('api.php');
                const movies = await response.json();
                moviesData = movies;
                slugToMovie.clear();
                moviesData.forEach(movie => {
                    // Generate slug if not present
                    if (!movie.slug) {
                        movie.slug = generateSlug(movie.title_en, movie.movie_id);
                    }
                    slugToMovie.set(movie.slug, movie);
                });
                // After loading, initialize the app
                initializeViewCounts();
                buildMarqueeContent();
                updateFooterTexts();
                updateAllMoviesH3();
                handleUrlChange();
                if (currentView === 'home') renderHomeView();
            } catch (error) {
                console.error('Failed to load movies:', error);
                moviesData = [];
                document.getElementById('moviesGrid').innerHTML = '<div class="no-results"><i class="fas fa-exclamation-triangle"></i><h3>Error loading movies</h3><p>Please check if the database is running.</p></div>';
            }
        }

        function initializeViewCounts() {
            const stored = localStorage.getItem('badini_view_counts');
            if (stored) {
                viewCounts = JSON.parse(stored);
            } else {
                moviesData.forEach((movie, idx) => {
                    const baseViews = Math.floor(Math.random() * 15000) + 5000;
                    viewCounts[movie.movie_id] = baseViews + (idx < 10 ? Math.floor(Math.random() * 20000) : 0);
                });
                saveViewCounts();
            }
        }

        function saveViewCounts() {
            localStorage.setItem('badini_view_counts', JSON.stringify(viewCounts));
        }

        function incrementViewCount(movieId) {
            viewCounts[movieId] = (viewCounts[movieId] || 0) + Math.floor(Math.random() * 5) + 1;
            saveViewCounts();
        }

        function escapeHtml(str) {
            if (!str) return '';
            return str.replace(/[&<>]/g, m => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;' }[m]));
        }

        function getDescription(movie) {
            return currentLanguage === 'ar' ? 
                (movie.description_ar || movie.description_en || 'لا يوجد وصف متاح لهذا الفيلم.') : 
                (movie.description_en || 'No description available for this film.');
        }

        function getTitle(movie) {
            return currentLanguage === 'ar' ? movie.title_ar : movie.title_en;
        }

        function updateAllMoviesH3() {
            const spanElement = document.getElementById('allMoviesList');
            if (!spanElement) return;
            const names = moviesData.map(movie => getTitle(movie));
            if (names.length === 0) {
                spanElement.textContent = currentLanguage === 'ar' ? 'لا توجد أفلام' : 'No movies';
                return;
            }
            spanElement.innerHTML = names.join(' · ');
        }

        function getFilteredMovies() {
            let filtered = moviesData.filter(movie => {
                if (currentFilter === 'all') return true;
                if (currentFilter === 'en') return movie.lang === 'en' || movie.lang === 'both';
                if (currentFilter === 'ar') return movie.lang === 'ar' || movie.lang === 'both';
                return true;
            });
            if (currentSearchQuery.trim() !== '') {
                const q = currentSearchQuery.trim().toLowerCase();
                filtered = filtered.filter(m => m.title_en.toLowerCase().includes(q) || m.title_ar.toLowerCase().includes(q));
            }
            return filtered;
        }

        function renderHomeView() {
            const filtered = getFilteredMovies();
            const totalPages = Math.ceil(filtered.length / ITEMS_PER_PAGE);
            if (currentPage > totalPages && totalPages > 0) currentPage = totalPages;
            else if (totalPages === 0) currentPage = 1;

            const startIndex = (currentPage - 1) * ITEMS_PER_PAGE;
            const paginatedMovies = filtered.slice(startIndex, startIndex + ITEMS_PER_PAGE);

            const moviesGrid = document.getElementById('moviesGrid');
            if (paginatedMovies.length === 0) {
                moviesGrid.innerHTML = `<div class="no-results"><i class="fas fa-film-slash"></i><h3>${currentLanguage === 'en' ? 'No movies found' : 'لا توجد أفلام'}</h3><p>${currentLanguage === 'en' ? 'Try adjusting search or filters.' : 'حاول تعديل البحث أو التصفية.'}</p></div>`;
            } else {
                moviesGrid.innerHTML = paginatedMovies.map(movie => {
                    const displayTitle = getTitle(movie);
                    const langClass = movie.lang === 'ar' ? 'ar' : (movie.lang === 'both' ? 'both' : 'en');
                    const views = viewCounts[movie.movie_id] || 0;
                    return `<div class="movie-card" onclick="openMovieDetail('${movie.slug}')">
                                <div class="video-thumbnail"><img src="${movie.poster_url}" alt="${escapeHtml(displayTitle)}" onerror="this.src='https://via.placeholder.com/300x180?text=No+Poster'"><div class="play-overlay"><i class="fas fa-play-circle"></i></div></div>
                                <div class="movie-details"><div class="movie-title"><span>${escapeHtml(displayTitle)}</span><span class="movie-year">${movie.year}</span></div>
                                <div class="movie-meta"><span class="lang-badge ${langClass}">${movie.lang === 'en' ? 'EN' : movie.lang === 'ar' ? 'AR' : 'EN/AR'}</span>
                                <span class="watch-counter"><i class="fas fa-eye"></i> ${views.toLocaleString()}</span><i class="fas fa-video" style="color:#4CAF50;"></i></div></div></div>`;
                }).join('');
            }

            const searchStats = document.getElementById('searchStats');
            if (currentSearchQuery.trim() !== '') {
                searchStats.innerHTML = currentLanguage === 'en' ? 
                    `Found ${filtered.length} movie(s) matching "${escapeHtml(currentSearchQuery)}"` : 
                    `تم العثور على ${filtered.length} فيلماً تطابق "${escapeHtml(currentSearchQuery)}"`;
            } else {
                searchStats.innerHTML = '';
            }

            renderPaginationControls(totalPages, filtered.length);
        }

        function renderPaginationControls(totalPages, totalItems) {
            const wrapper = document.getElementById('paginationWrapper');
            if (!wrapper) return;
            if (totalPages <= 1) { wrapper.innerHTML = ''; return; }

            const isEn = currentLanguage === 'en';
            let buttonsHtml = '';
            buttonsHtml += `<button class="pagination-btn prev-btn ${currentPage === 1 ? 'disabled' : ''}" data-page="prev"><i class="fas fa-chevron-left"></i> ${isEn ? 'Prev' : 'السابق'}</button>`;
            let startPage = Math.max(1, currentPage - 2);
            let endPage = Math.min(totalPages, startPage + 4);
            if (endPage - startPage < 4) startPage = Math.max(1, endPage - 4);
            for (let i = startPage; i <= endPage; i++) {
                buttonsHtml += `<button class="pagination-btn ${i === currentPage ? 'active-page' : ''}" data-page="${i}">${i}</button>`;
            }
            buttonsHtml += `<button class="pagination-btn next-btn ${currentPage === totalPages ? 'disabled' : ''}" data-page="next"><i class="fas fa-chevron-right"></i> ${isEn ? 'Next' : 'التالي'}</button>`;
            const startItem = (currentPage - 1) * ITEMS_PER_PAGE + 1;
            const endItem = Math.min(currentPage * ITEMS_PER_PAGE, totalItems);
            const infoSpan = `<span class="page-info"><i class="fas fa-film"></i> ${startItem}-${endItem} / ${totalItems}</span>`;
            wrapper.innerHTML = buttonsHtml + infoSpan;
            document.querySelectorAll('.pagination-btn').forEach(btn => {
                btn.removeEventListener('click', paginationClickHandler);
                btn.addEventListener('click', paginationClickHandler);
            });
        }

        function paginationClickHandler(e) {
            const btn = e.currentTarget;
            if (btn.classList.contains('disabled')) return;
            const pageVal = btn.dataset.page;
            const filteredTotal = getFilteredMovies().length;
            const totalPages = Math.ceil(filteredTotal / ITEMS_PER_PAGE);
            let newPage = currentPage;
            if (pageVal === 'prev') newPage = Math.max(1, currentPage - 1);
            else if (pageVal === 'next') newPage = Math.min(totalPages, currentPage + 1);
            else if (!isNaN(parseInt(pageVal))) newPage = parseInt(pageVal);
            if (newPage !== currentPage && newPage >= 1 && newPage <= totalPages) {
                currentPage = newPage;
                renderHomeView();
            }
        }

