        function generateAndDownloadSitemap() {
            const baseUrl = window.location.origin + window.location.pathname.replace(/\/[^\/]*$/, '/');
            const cleanBaseUrl = baseUrl.split('?')[0];
            const today = new Date().toISOString().split('T')[0];

            let urls = `
  <url>
    <loc>${cleanBaseUrl}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>daily</changefreq>
    <priority>1.0</priority>
  </url>`;

            moviesData.forEach(movie => {
                const movieUrl = `${cleanBaseUrl}?movie=${movie.slug}`;
                urls += `
  <url>
    <loc>${movieUrl}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>weekly</changefreq>
    <priority>0.8</priority>
  </url>`;
            });

            const sitemapXml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">${urls}
</urlset>`;

            const blob = new Blob([sitemapXml], { type: 'application/xml' });
            const link = document.createElement('a');
            link.href = URL.createObjectURL(blob);
            link.download = 'sitemap.xml';
            document.body.appendChild(link);
            link.click();
            document.body.removeChild(link);
            URL.revokeObjectURL(link.href);

            alert(currentLanguage === 'en' ? 'sitemap.xml generated and downloaded successfully!' :
                'تم إنشاء وتحميل ملف sitemap.xml بنجاح!');
        }

