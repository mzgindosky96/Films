        function updateUrlForMovie(movieSlug) {
            const url = new URL(window.location);
            if (movieSlug) {
                url.searchParams.set('movie', movieSlug);
            } else {
                url.searchParams.delete('movie');
            }
            window.history.pushState({ movieSlug: movieSlug || null }, '', url);
        }

